#include<iostream>
using namespace std;

int main()
{
    short s;
    int i;
    long l;
    char c;
    double d;
    float f;
    cout<<"size of short : "<<sizeof(s)<<endl;
    cout<<"size of int : "<<sizeof(i)<<endl;
    cout<<"size of long : "<<sizeof(l)<<endl;
    cout<<"size of char : "<<sizeof(c)<<endl;
    cout<<"size of double : "<<sizeof(d)<<endl;
    cout<<"size of float : "<<sizeof(f)<<endl;
    return 0;
}
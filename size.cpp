#include<iostream>
using namespace std;
int main()
{
    int n;
    float f;
    double d;
    long l;
    short s;
    char c;
    cout<<"the size of int: "<<sizeof(n)<<endl;
    cout<<"the size of float: "<<sizeof(f)<<endl;
    cout<<"the size of double: "<<sizeof(d)<<endl;
    cout<<"the size of long: "<<sizeof(l)<<endl;
    cout<<"the size of short: "<<sizeof(s)<<endl;
    cout<<"the size of char: "<<sizeof(c)<<endl;
    return 0;
}